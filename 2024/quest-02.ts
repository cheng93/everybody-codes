function solve1(input: string) {
    return solve(input, false, (register, splits) => {
        return [...splits[2]].reduce((agg, cur) => {
            register.update(cur);
            return agg + register.getMatches().length;
        }, 0);
    });
}

function solve2(input: string) {
    return solve(input, true, (register, splits) => {
        return splits.slice(2).reduce((agg, line) => {
            register.reset();
            const indices = new Set();
            [...line].forEach((letter, i) => {
                register.update(letter);
                const match = register.getMatch();
                if (match) {
                    for (var j = 0; j < match; j++) {
                        indices.add(i - j);
                    }
                }
            });
            return agg + indices.size;
        }, 0);
    });
}

function solve3(input: string) {
    return solve(input, true, (register, splits, words) => {
        const points = new Set();
        const biggestWord = Math.max(...words.map((x) => x.length));
        const grid = splits.slice(2);

        for (var i = 0; i < grid[0].length; i++) {
            register.reset();
            for (var j = 0; j < grid.length; j++) {
                const letter = grid[j][i];
                register.update(letter);
                const match = register.getMatch();
                if (match) {
                    for (var k = 0; k < match; k++) {
                        points.add(`${j - k}|${i}`);
                    }
                }
            }
        }

        for (var i = 0; i < grid.length; i++) {
            register.reset();
            const line = grid[i];
            for (var j = 0; j < line.length + biggestWord; j++) {
                const letter = line[j % line.length];
                register.update(letter);
                const match = register.getMatch();
                if (match) {
                    for (var k = 0; k < match; k++) {
                        points.add(
                            `${i}|${(j + line.length - k) % line.length}`
                        );
                    }
                }
            }
        }

        return points.size;
    });
}

function solve(
    input: string,
    reverse: boolean,
    callback: (register: Register, splits: string[], words: string[]) => number
) {
    const splits = input.split('\n');
    let words = splits[0].substring(6).split(',');
    words = reverse
        ? [...words, ...words.map((x) => [...x].reverse().join(''))]
        : words;
    const register = new Register(words);

    return callback(register, splits, words);
}

class Register {
    constructor(words: string[]) {
        this.#registers = words.reduce((agg, cur) => {
            agg[cur.length] = '';
            return agg;
        }, {});
        this.#set = new Set(words);
    }

    #registers: { [key: number]: string };
    #set: Set<string>;

    getMatches(): number[] {
        return Object.entries(this.#registers)
            .filter(
                ([key, value]) => value.length === +key && this.#set.has(value)
            )
            .map((x) => +x[0]);
    }

    getMatch(): number | undefined {
        return this.getMatches().pop();
    }

    reset() {
        Object.keys(this.#registers).forEach((x) => (this.#registers[+x] = ''));
    }

    update(letter: string) {
        Object.keys(this.#registers)
            .map((x) => +x)
            .forEach((x) => {
                if (this.#registers[x].length === x) {
                    this.#registers[x] = this.#registers[x].substring(1);
                }
                this.#registers[x] += letter;
            });
    }
}

